void main() {
  //! for loop statement
  for (int i = 0; i < 10; i++) {
    // tis will exuted 10 times .
    print("Hellow ");
  }

  //! while loop
  int iterator = 0;
  while (iterator < 10) {
    print("Helowwwww");
    iterator++;
  }

  //! do- while loop
  // run one time even the condtion passed is wrong
  int i = 0;
  do {
    print("helllllllow");
    i++;
  } while (i < 10);
}
